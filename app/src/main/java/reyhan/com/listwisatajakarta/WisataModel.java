package reyhan.com.listwisatajakarta;

public class WisataModel {
    private String namaWisata;
    private String lokasiWisata;
    private String descWisata;
    private String gambarWisata;

    public String getNamaWisata() {
        return namaWisata;
    }

    public void setNamaWisata(String namaWisata) {
        this.namaWisata = namaWisata;
    }

    public String getNamaLokasiWisata() {
        return lokasiWisata;
    }

    public void setNamaLokasiWisata(String namaLokasiWisata) {
        this.lokasiWisata = namaLokasiWisata;
    }

    public String getDescWisata() {
        return descWisata;
    }

    public void setDescWisata(String descWisata) {
        this.descWisata = descWisata;
    }

    public String getGambarWisata() {
        return gambarWisata;
    }

    public void setGambarWisata(String gambarWisata) {
        this.gambarWisata = gambarWisata;
    }
}

